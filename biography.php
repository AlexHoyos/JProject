                <h2>Biografía</h2>
                <p>En la década de los cincuentas en Aguascalientes nació José de Jesús Palacios Garcia, mejor conocido en el mundo de la plástica como Jesús Palacios, desde niño mostró inclinación al dibujo y la pintura. Este artista fue un joven curioso, inquieto, atento a los detalles de su entorno, de personalidad solitaria, Chuy se relacionó de manera activa con la madre-naturaleza, con las flores, campamochas, tlacuaches y por supuesto con el enigmático cielo de la región del centro de México, así se comenzó a bocetar su pródiga carrera iconográfica.<br><br>
                
                En el año de 1979 Jesús Palacios, se decidió profesionalmente e inició sus estudios formales de pintura en la Casa del Lago, en la Ciudad de México, paralelamente cursó su carrera en la Escuela Nacional de Pintura, Escultura y Grabado: La Esmeralda. Él se destacó en el oficio de la pintura. Se proyectó como artista independiente y simultáneamente se incorporó a la labor docente.<br><br>

                En la actualidad este artista cuenta con 76 exposiciones colectivas en el país y 35 individuales. Su obra ha sido divulgada en importantes recintos entre los que destacan: Museo de Arte Contemporáneo, Museo de la Insurgencia, Museo de Historia, todos ellos en su natal Aguascalientes. <br><br></p> 
            
