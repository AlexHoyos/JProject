                <p>Asimismo ha expuesto su trabajo creador en diversos museos de orden nacional; su obra también ha surcado los mares para departirse en el: Busan internacional Print Art Festival, The Korea Fundation Cultural Center y en la Galeria BELLART en el mistíco pais de Corea del Sur. Fue residente de la beca Vermont, Studio Center, U.S.A en 2005 y recientemente fue estimulado con una beca del Programa a la Creación y Desarrollo Artistico, de donde derivó obra de gran formato; además de obtener premio en la Séptima Bienal "Enrique Guzmán". <br><br>

                La producción visual de Jesús Palacios, se bifurca contundentemente hacia dos estilos opuestos y complementarios: figurativismo y abstracto. En el primero con una propensión costumbrista, en donde aparecen los interminables cielos del campo hidrocálido, su vegetación, fauna y, por supuesto su gente. En un extremo opuesto su estilo abstracto traza pinceladas con libertad y fuerza. Él explora, plantea y juega con formas, colores veladuras y texturas, para crear un producto de manufactura plástica contundentemente armónico, va generando efervescencia emocional, que evoca a la profundidad de su introspectiva personalidad reflexiva, creando a la vez sútiles atmósferas, parábola silente que nos conducen de retorno a ese misterioso y apartado mundo entre nopales y huizaches. <br><br><br>
